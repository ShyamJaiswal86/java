// Arithmetic Operators

// Binary -> +, -, *, /, %
// Unary -> ++, --
// Relational -> ==, !=, >, <, <=, >=       Return True/False (boolean value)
// Logical -> &&, ||, !
// Bitwise -> &(and), |(or), ^(xor), ~(compliment), <<(left shift), >>(right shift)

public class Example{
    public static void main(String[] args){
        
    //Pre increment 
    int a = 10;
    int b = 0;
    b = ++a;
    System.out.println(a);
    System.out.println(b);

    //Post increment
    int c = 10;
    int d = 0;
    d = c++;
    System.out.println(c);
    System.out.println(d);

    }
}



